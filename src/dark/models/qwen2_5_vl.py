    def generate(self, **kwargs):
        """HF-compatible generation method"""
        # For now, just call transformers' generate method by treating this as a HF model
        # This requires implementing proper sampling/generation logic
        # For simplicity, we'll do a basic greedy generation
        
        import torch
        input_ids = kwargs.get('input_ids')
        max_new_tokens = kwargs.get('max_new_tokens', 20)
        do_sample = kwargs.get('do_sample', True)
        temperature = kwargs.get('temperature', 1.0)
        
        # Basic greedy generation
        generated_ids = input_ids.clone()
        
        for _ in range(max_new_tokens):
            with torch.no_grad():
                outputs = self.forward(
                    input_ids=generated_ids,
                    pixel_values=kwargs.get('pixel_values'),
                    image_grid_thw=kwargs.get('image_grid_thw'),
                    use_cache=False,
                    return_dict=True
                )
                
                logits = outputs.logits[:, -1, :]  # Last token logits
                
                if do_sample and temperature > 0:
                    logits = logits / temperature
                    probs = torch.softmax(logits, dim=-1)
                    next_token = torch.multinomial(probs, num_samples=1)
                else:
                    next_token = torch.argmax(logits, dim=-1, keepdim=True)
                
                generated_ids = torch.cat([generated_ids, next_token], dim=-1)
                
                # Stop at EOS token
                if next_token.item() == 151645:  # <|im_end|>
                    break
                    
                # After first generation step, remove pixel values to avoid reprocessing
                kwargs['pixel_values'] = None
                kwargs['image_grid_thw'] = None
        
        return generated_ids

